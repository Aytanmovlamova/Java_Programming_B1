public class TabAnd {


    public static void main(String[] args) {

        //This is without any tab
        System.out.println("This is not tabbed");

        //This is without one tab
        System.out.println("\tThis is not tabbed");

        //This is without two tab
        System.out.println("\t\tThis is not tabbed");

        //This is without any tab
        System.out.println("                   This is tabbed without escape sequence");

        //examples of new line
        System.out.println();
        System.out.println("1) Go to parking lot");
        System.out.println("2) Find the car");
        //examples of new line
        System.out.println();
        System.out.println("\n1) Go to parking lot");System.out.println("\n2) Find the car");



        ;







    }
}










